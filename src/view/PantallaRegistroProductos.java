    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        try{
            int id = Integer.parseInt(tfID.getText());
            String nombre = tfNombre.getText();
            String categoria = tfNombre.getText();
            double precio = Double.parseDouble(tfPrecio.getText());
            Object temp = spinnerStock.getValue();
            int stock = Integer.parseInt(temp.toString());
            if (stock < 1){
                JOptionPane.showMessageDialog(this, "Ingresa una cantidad vÃ¡lida");
                return;
            }
            if (precio < 0){
                JOptionPane.showMessageDialog(this, "El precio no puede ser menor a 0");
                return;
            }
            Producto nuevo = new Producto(id, nombre, precio, categoria, stock);
            elInventario.agregar(nuevo);
            JOptionPane.showMessageDialog(this, nombre+" registrado correctamente.");
            this.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosed(java.awt.event.WindowEvent e) {
                principal.notificaciones(4);
            }
            });
            dispose();
        }catch(HeadlessException | NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Hay fallas en el formato de entrada", "Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

