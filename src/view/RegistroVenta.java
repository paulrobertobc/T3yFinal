    private void btnRegistrarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarVentaActionPerformed
        // TODO add your handling code here:
        String nombre = tfNombre.getText();
        String apellido = tfApellido.getText();
        String dni = tfDni.getText();
        String telefono = tfTelefono.getText();
        String email = tfEmail.getText().trim();
        String patron = "^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$";
        if (camposVacios()){
            JOptionPane.showMessageDialog(this, "Llena los campos.");
            return;
        }
        if (!email.matches(patron)) {
            JOptionPane.showMessageDialog(this, "Correo inválido ❌");
            return;
        }
        if (dni.length() != 8){
            JOptionPane.showMessageDialog(this, "DNI inválido ❌\nDebe ser de 8 dígitos.");
            return;
        }
        if (nombre.isEmpty() || dni.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete los datos del cliente.");
            return;
        }
        if (listaMientras.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No hay productos para registrar.");
            return;
        }
        for (String item : listaMientras) {
            String[] partes = item.split("\\|");
            String nombreProd = partes[0].trim();
            Producto producto = in.getListaProductosInven().stream().filter(p -> p.Nombre.equalsIgnoreCase(nombreProd)).findFirst().orElse(null);
            if (producto == null) {
                JOptionPane.showMessageDialog(this, "El producto " + nombreProd + " no existe en inventario.");
                return;
            }
            if (producto.Stock <= 0) {
                JOptionPane.showMessageDialog(this, "El producto " + nombreProd + " está sin stock.");
                return;
            }
        }
        for (String item : listaMientras) {
            String[] partes = item.split("\\|");
            String nombreProd = partes[0].trim();
            for (Producto p : in.getListaProductosInven()) {
                if (p.Nombre.equalsIgnoreCase(nombreProd)) {
                    p.Stock -= 1;
                if (p.Stock < 0) p.Stock = 0;
                }
            }
        }
        int id = crearID();
        Cliente nuevo = new Cliente(id,total,nombre, apellido, dni, telefono, email);
        nuevo.agregarCompra(listaMientras);
        controlador.agregarAlistaCliente(nuevo);
        Venta nuevaVenta = new Venta();
        nuevaVenta.agregarVentayFecha(id,listaMientras, ahora, nuevo);
        controlador.agregarAlistaVenta(nuevaVenta);
        listaMientras.clear();
        llenadoLista();
        mesero.atendidos = mesero.atendidos + 1;
        cajero.atendidos = cajero.atendidos + 1;
        controlador.setCapital(total);
        if (checkRecibo.isSelected()){
            nuevaVenta.guardarBoleta();
        }
        
        JOptionPane.showMessageDialog(this, "Venta registrada correctamente a " + nuevo.getNombre());
        this.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosed(java.awt.event.WindowEvent e) {
                principal.notificaciones(1);
            }
        });
        dispose();
    }//GEN-LAST:event_btnRegistrarVentaActionPerformed

    private void btnAgregarPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarPedidoActionPerformed
        // TODO add your handling code here:
        String cliente = (String) cbProductos.getSelectedItem();
        if (camposVacios()){
            JOptionPane.showMessageDialog(this, "Llena los campos.");
            return;
        }
        var a = in.listaProductos.stream().filter(p -> p.Nombre.equalsIgnoreCase(cliente)).findFirst().orElse(null);
        if (a == null) {
            JOptionPane.showMessageDialog(this, "No seleccionaste nada.");
            return;
        }
        long stockActual = listaMientras.stream().filter(p -> p.startsWith(a.getNombre() + " |")).count();
        if (stockActual >= a.getStock()) {
            JOptionPane.showMessageDialog(this, "No hay suficiente stock de " + a.getNombre());
            return;
        }
        listaMientras.add(a.getNombre() + " | " + a.getCategoria() + " | " + a.getPrecio());
        llenadoLista();
    }//GEN-LAST:event_btnAgregarPedidoActionPerformed

    public int crearID(){
        int a_base = random.nextInt(90000);
        int id = a_base + 10000;
        boolean a = controlador.listaVenta.stream().anyMatch(p->p.getID() == id);
        if (a == true){
            crearID();
        }
        return id;
    }
    
    public void llenadoLista() {
        calcularTotalListaTemporal();
        modeloLista.clear();
        for (String p : listaMientras) {
            modeloLista.addElement(p);
        }
        lbTotal.setText("Total :"+total);
    }
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int indice = ListaPedidoMientras.getSelectedIndex();
        if (indice != -1) {
            listaMientras.remove(indice);
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona un elemento para eliminar");
        }
        llenadoLista();
    }//GEN-LAST:event_btnEliminarActionPerformed

    public void calcularTotalListaTemporal() {
        total = 0;
        for (String item : listaMientras) {
            String[] partes = item.split("\\|"); // separa por |
            if (partes.length >= 2) {
                try {
                    double precio = Double.parseDouble(partes[2].trim()); // convertimos a double
                    total += precio;
                }catch (NumberFormatException e) {
                    System.out.println("Error al convertir precio: " + partes[2]);
                }
            }
        }
    }
