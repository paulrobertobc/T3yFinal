    public void agregarCb(JComboBox<String> combo, String nombre) {
        boolean existe = false;
        for (int i = 0; i < combo.getItemCount(); i++) {
            if (combo.getItemAt(i).equalsIgnoreCase(nombre)) { 
                existe = true;
                break;
            }
        }
        if (!existe) {
            combo.addItem(nombre);
        }
    }
