    private void elComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_elComboBoxActionPerformed
        // TODO add your handling code here:
        String selec = elComboBox.getSelectedItem().toString();
        nombreTemporal = selec;
        var a = listaProductos.stream().filter(p->p.getNombre().equalsIgnoreCase(selec)).findFirst().orElse(null);
        if (a != null){
            tfNombre.setText(a.getNombre());
            tfCategoria.setText(a.getCategoria());
            String precio = String.valueOf(a.getPrecio());
            tfPrecio.setText(precio);
            spinnerStock.setValue(a.getStock());
        }
    }//GEN-LAST:event_elComboBoxActionPerformed

