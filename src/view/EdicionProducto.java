    private void btnEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditarMouseClicked
        // TODO add your handling code here:
        try{
            Object temp = spinnerStock.getValue();
            int stock = Integer.parseInt(temp.toString());
            double precio = Double.parseDouble(tfPrecio.getText());
            if (stock < 1 || precio < 0){
                JOptionPane.showMessageDialog(this, "Ingresa cantidades vÃ¡lidas.");
                return;
            }
            var a = listaProductos.stream().filter(p->p.getNombre().equalsIgnoreCase(nombreTemporal)).findFirst().orElse(null);
            if (a != null){
                a.setNombre(tfNombre.getText());
                a.setCategoria(tfCategoria.getText());
                a.setPrecio(precio);
                a.setStock(stock);
                JOptionPane.showMessageDialog(this, nombreTemporal+" fue actualizado");
                dispose();
            }else{
                JOptionPane.showMessageDialog(this, "No existe");
            }
        }catch(HeadlessException | NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Hay error");
        }
        
    }//GEN-LAST:event_btnEditarMouseClicked

