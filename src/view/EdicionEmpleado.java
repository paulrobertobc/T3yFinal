    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        try{
            String nombre = tfNombre.getText();
            String apellido = tfApellido.getText();
            String dni = tfDNI.getText();
            String telefono = tfTelefono.getText();
            String email = tfEmail.getText().trim();
            String patron = "^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,}$";
            double sueldo = Double.parseDouble(tfSueldo.getText());
            String selec = (String)cbElegirTipo.getSelectedItem();
            if (dni.length() != 8){
                JOptionPane.showMessageDialog(this, "El DNI debe ser de 8 dígitos.");
                return;
            }
            if (!email.matches(patron)) {
                JOptionPane.showMessageDialog(this, "Correo inválido ❌");
                return;
            }
            if(sueldo < 0){
                JOptionPane.showMessageDialog(this, "El sueldo no puede ser menos que 0");
                return;
            }
            var a = listaT.stream().filter(p->p.getNombre().equalsIgnoreCase(nombreTemporal)).findFirst().orElse(null);
            if (a != null){
                a.setNombre(nombre);
                a.setApellido(apellido);
                a.setDNI(dni);
                a.setTelefono(telefono);
                a.setEmail(email);
                a.setSueldo(sueldo);
                JOptionPane.showMessageDialog(this, nombreTemporal+" fue actualizado");
                
                if (rbMeseros.isSelected()){
                    if (selec.equalsIgnoreCase("Cajeros")){
                        listaCajeros.add(a);
                        a.setTipo("Cajero");
                    }
                    if (selec.equalsIgnoreCase("Cocineros")){
                        listaCocineros.add(a);
                        a.setTipo("Cocinero");
                    }
                    listaMeseros.remove(a);
                }
                if (rbCocineros.isSelected()){
                    if (selec.equalsIgnoreCase("Meseros")){
                        listaMeseros.add(a);
                        a.setTipo("Mesero");
                    }
                    if (selec.equalsIgnoreCase("Cajeros")){
                        listaCajeros.add(a);
                        a.setTipo("Cajero");
                    }
                    listaCocineros.remove(a);
                }
                if (rbCajeros.isSelected()){
                    if (selec.equalsIgnoreCase("Meseros")){
                        listaMeseros.add(a);
                        a.setTipo("Mesero");
                    }
                    if (selec.equalsIgnoreCase("Cocineros")){
                        listaCocineros.add(a);
                        a.setTipo("Cocinero");
                    }
                    listaCajeros.remove(a);
                }
                this.addWindowListener(new java.awt.event.WindowAdapter() {
                    @Override
                    public void windowClosed(java.awt.event.WindowEvent e) {
                        principal.notificaciones(3);
                    }
                });
                dispose();
            }
            else{
                JOptionPane.showMessageDialog(this, "No existe");
            }
        } catch (HeadlessException | NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Hubieron errores en el registro.");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

