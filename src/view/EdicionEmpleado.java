    public void agregarCb(JComboBox<String> elComboBox, String nombre) {
        
        boolean existe = false;
        for (int i = 0; i < elComboBox.getItemCount(); i++) {
            if (elComboBox.getItemAt(i).equalsIgnoreCase(nombre)) { 
                existe = true;
                break;
            }
        }
        if (!existe) {
            elComboBox.addItem(nombre);
        }
    }
    public void llenadoTF(ArrayList<Empleado> listaT){
        String selec = elComboBox.getSelectedItem().toString();
        nombreTemporal = selec;
        var a = listaT.stream().filter(p->p.getNombre().equalsIgnoreCase(selec)).findFirst().orElse(null);
        if (a != null){
            tfNombre.setText(a.getNombre());
            tfApellido.setText(a.getApellido());
            tfDNI.setText(a.getDNI());
            tfTelefono.setText(a.getTelefono());
            tfEmail.setText(a.getEmail());
            cbElegirTipo.setSelectedItem(a.getTipo());
            tfSueldo.setText(String.valueOf(a.getSueldo()));
        }
    }
